# Convert all text-like files from CRLF to LF.

find . \
\( \
	-name '*.C' -o \
	-name '*.H' -o \
	-name '*.HPP' -o \
	-name '*.INC' -o \
	-name '*.md' -o \
\) \
! -path '*/.git/*' \
-type f \
-exec sh -c '
for __THE_FILE__
do
	echo "$__THE_FILE__"
	tr -d "\015" < "$__THE_FILE__" > "$__THE_FILE__.TMP" &&
	mv "$__THE_FILE__.TMP" "$__THE_FILE__"
done
' sh {} \;
